SELECT SUM(COMMISSION) AS"커미션 총액"
FROM EMPLOYEE;

SELECT COUNT(*) AS"사원의 수" --NULL포함한 모든행의 갯수
FROM EMPLOYEE;

SELECT COUNT(COMMISSION) AS"커미션 받는사원수"
