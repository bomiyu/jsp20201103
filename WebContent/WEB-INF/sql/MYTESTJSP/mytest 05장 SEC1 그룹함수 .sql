SELECT COUNT(DISTINCT JOB) AS"직업종류의 갯수" --JOB중복제외 
 FROM EMPLOYEE;
 
 SELECT ENAME, MAX(SALARY) -- 그룹함수아닌것을 포함 시키면 실행ERROR
 FROM EMPLOYEE;
 
 SELECT DNO AS"부서번호", AVG(SALARY) AS"급여평균"--소속 부서별 평균 급여를 부서번호와 함께 출력 
 FROM EMPLOYEE
 GROUP BY DNO;
 
 SELECT avg(salary) as"급여평균" --소속 부서별 평균급여 구하기
 from employee
